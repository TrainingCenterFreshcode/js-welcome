// !== ("не дорівнює", "not equal")
// Бінарний оператор, повертає boolean
// Перевіряє, чи обидва операнди НЕ рівні одне одному
// Якщо операнди НЕ рівні між собою - повертається true
// Якщо операнди рівні між собою - повертається false

// Задача. Дано число. Перевірити, чи це число рівне 5.

const number = 10;

if(number !== 5) {
    console.log(number + ', число не дорівнює 5');
} else {
    console.log(number + ', число дорівнює 5');
}